        <div>
            <p>There are many reasons why most of us prefer buying used cars over brand new ones.</p>
            <p>The lower prices, cheaper registration fees and so much more!</p>
            <p>However, buying a used car that has passed its warranty date or has little to no service history can be risky. </p>
            <p>One might ask, how do I know if this used car is safe to buy? How do I know if this care will function properly once purchased? Where can I get a used car inspected before buying it?</p>
        </div>
        <br>
        <div>
            <p>Here are some signs you need to watch out for when buying a used car as per some automotive specialists:</p>
            <ol>
                <li>
                    <strong>Paint issues</strong>
                    - Checking a vehicle inside the showroom can be deceiving. This is because the lighting inside the showroom can exaggerate the paintwork of a car. It is advisable to take the car outdoors in the presence of natural sunlight to see how it really is. If you identify a mismatch of colors between panels, it means that it has been in a bodywork repair.
                </li>
                <li>
                    <strong>Strange car noises</strong> - Always take the used car for a test drive before purchasing it. If you hear strange noises like loud engines, knockings, sharp squealing to name a few, it could mean that the vehicle is damaged. If you’re confused about what these noises sound like, you can always approach independent service centres like <a href="./index.php">Gargash Auto</a> for help.
                    <br>
                    <br>
                    <p>Check out our blog on ‘car noises to watch out for’ to know more! </p>
                </li>
                <li>

                    <strong>Panel gaps</strong> – in case a car has gone through a critical accident, it may not be possible to perfectly align the car during repair. To check the car’s alignment, check if there is a gap between the car bonnets and the panels. This can be done by placing your finger in the gap to see if it fits, if it does, it indicates that the car is not perfectly aligned. In addition, you can also check for gaps between car doors using the same method.
                </li>
                <li>
                    <strong>Replaced parts</strong> - Brand new parts on a largely worn and aged vehicle is a big warning against purchasing a used car. Some parts that you can easily identify are the headlights, taillights, brake lights and even the airbag cover. Mismatched or third-party parts are also used as an attempt to lower repair costs and this is why you should always verify these irregularities with the seller. It is recommended to involve <a href="./about-us.php">automotive experts</a> to guide you during the purchase as they might identify problems you may not be able to.
                </li>
                <li>
                    <strong>Car pulling to one side</strong> - As mentioned earlier, driving a used car before purchasing it is important as this would bring up potential problems with the vehicle if any. If the car pulls on a side or does not drive straight, it either indicates a misalignment or (worst case scenario) a bent chassis that is impossible to align in most cases.
                </li>
            </ol>
            <br>
